    private void selectData() {
        String kolom[] = {"nama","alamat","struk","nopol","tgl_pinjam","tgl_kembali","lama_pinjam","harga"};
       DefaultTableModel dtm = new DefaultTableModel(null, kolom);
       String SQL = "SELECT * FROM db_uts";
       ResultSet rs = KoneksiDB.executeQuery(SQL);
       try{
           while(rs.next()){
               String Nama = rs.getString(1);
               String Alamat = rs.getString(2);
               String STRUK = rs.getString(3);
               String Pol = rs.getString(4);
               String Pinjam = rs.getString(5);
               String Kembali = rs.getString(6);
               String Lama = rs.getString(7);
               String Harga = rs.getString(8);
               String data [] = {Nama,Alamat,STRUK,Pol,Pinjam,Kembali,Lama,Harga};
               dtm.addRow(data);
           }   
        }catch(SQLException ex){
            Logger.getLogger(rental.class.getName()).log(Level.SEVERE, null, ex);
        }
       tbRental.setModel(dtm);

    }

